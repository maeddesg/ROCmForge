// ── Embedding lookup ───────────────────────────────────────────────────────
// Copies embedding_table[token_id * hidden_dim + d] → output[seq_pos * hidden_dim + d].
// Thread grid: (hidden_dim / 256, seq_len, 1) × 256 threads.

